public class doubleforloop
{
    public static void main(String [] args)
    {
        int i=0,j=0;
        for(i=0,j=0; i<=5 && j<=10 ;i++ ,j++)
        {
            //we cant write the , in this...we can only write the the || and and && 
            System.out.println(i + " " + j);
        }
    }
}